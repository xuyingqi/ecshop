<script type="text/javascript">
    window.onload= function () {
        var box=document.getElementById("scroll");
        var ul=box.children[0];
        //取得大盒子的宽度
        var style=getComputedStyle(box);
        var boxW=parseFloat(style.width);
        //取得播放的组数
        var imgs=ul.children.length-2;
        var pages=Math.ceil(imgs/2);
        //定义定时器序号timer
        var timer=null;
        //定义ul左侧的初始值
        var num=0;
        timer=setInterval(autoPlay,5);
        function autoPlay(){
            num--;
            num<=-boxW*pages?num=0:num;
            ul.style.left=num+"px";
        }
        box.onmouseover= function () {
            clearInterval(timer);
        }
        box.onmouseout= function () {
            timer=setInterval(autoPlay,5);
        }
    }
</script>
